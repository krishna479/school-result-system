    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            String rollNO=jTextField1.getText();
            Connectioncode obj = new Connectioncode();
            Connection con = obj.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs =st.executeQuery("select * from result where rollNO='"+rollNO+"'");
            
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Result Does not exits");
                
            }
            else{
                setVisible(false);
                StudentResultPage frame =new StudentResultPage(rollNO);
                frame.setVisible(true);
                frame.setLocationRelativeTo(null);
            }
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

